public class Using_Numbers
{
    public static void main(String [] args){
        // using numbers and comparing them in if-else statments
        
        //The different relational and logic operations
        // <, <=, >, >=, ==, !=
        
        //Less than
        if(2 < 3){
            System.out.println("if: 2 is less than 3");
        }
        else{
            System.out.println("This won't be printed");
        }
        
        //Less than or equal to
        if(2 <= 3){
            System.out.println("if: 2 is less than or equal to 3");
        }
        else{
            System.out.println("This won't be printed");
        }
        
        //Greater than
        if(2 > 3){
            System.out.println("This won't be printed");
        }
        else{
            System.out.println("else: 2 is not greater than 3");
        }
        
        //Greater than or equal to
        if(2 >= 3){
            System.out.println("This won't be printed");
        }
        else{
            System.out.println("else: 2 is not greater than or equal to 3");
        }
        
        //Equal too
        if(2 == 3){
            System.out.println("This won't be printed");
        }
        else{
            System.out.println("else: 2 is not equal to 3");
        }
        
        //Not equal too
        if(2 != 3){
            System.out.println("if: 2 is not eqaul to 3");
        }
        else{
            System.out.println("This won't be printed");
        }
    }
}
